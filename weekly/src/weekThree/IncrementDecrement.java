package weekThree;
import java.util.Scanner;

public class IncrementDecrement {
	public static void main(String[] args) {
		
		int x = 5;
		int y = 10;
		
		System.out.println("The value of x is " + x);
		System.out.println("The value of ++x is " + ++x);
		System.out.println("The value of x++ is " + x++);
		System.out.println("The value of x is " + x);
		System.out.println("The value of y is " + y);
		System.out.println("The value of --y is " + --y);
		System.out.println("The value of y-- is " + y--);
		System.out.println("The value of y is " + y);
		
		
	}

}
